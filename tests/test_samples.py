def is_valid_grid(grid):
    # rows
    for r in grid:
        if set(r) != set(range(1, 10)):
            return False
    # cols
    for c in range(9):
        if set(grid[r][c] for r in range(9)) != set(range(1, 10)):
            return False
    # boxes
    for br in range(3):
        for bc in range(3):
            vals = []
            for r in range(br * 3, br * 3 + 3):
                for c in range(bc * 3, bc * 3 + 3):
                    vals.append(grid[r][c])
            if set(vals) != set(range(1, 10)):
                return False
    return True


def test_generated_puzzles_solve():
    for idx, p in enumerate(PUZZLES, 1):
        s = Sudoku(p)
        solved = s.solve(max_rounds=200)
        assert solved, f"Puzzle {idx} did not solve with logic-only strategies"
        grid = s.to_grid()
        assert is_valid_grid(grid), f"Puzzle {idx} solved to an invalid grid"
